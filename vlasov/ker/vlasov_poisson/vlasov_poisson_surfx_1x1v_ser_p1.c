  Ghat_r[2] = (1.224744871391589*fc[5]+0.7071067811865475*fc[4])*wv+(0.3162277660168379*fc[3]+0.18257418583505536*fc[2])*dv; 

  Ghat_l[0] = (1.224744871391589*fl[1]+0.7071067811865475*fl[0])*wv+(0.3535533905932737*fl[3]+0.20412414523193148*fl[2])*dv; 
